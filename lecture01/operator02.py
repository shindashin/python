#관계 연산자
#(1) 동등 비교
num1 = 100
num2 = 20
bool_result = num1 == num2
print(bool_result)
bool_result = num1 != num2
print(bool_result)

#(2) 크기 비교
bool_result = num1 > num2 # num1 값이 큰지 비교
print(bool_result)

bool_result = num1 >= num2 # num1 값이 크거나 같은지 비교
print(bool_result)
bool_result = num1 < num2 # num2 값이 큰지 비교
print(bool_result)
bool_result = num1 <= num2 # num2 값이 크거나 같은지 비교
print(bool_result)


