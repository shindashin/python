num1 = 100  # 피연산자1
num2 = 20 # 피연산자2
# 사칙연산
add = num1 + num2 # 덧셈 연산

print('add = ' , add)
sub = num1 - num2 # 뺄셈 연산
print('sub = ' , sub)
mul = num1 * num2 #곱셈 연산
print('mul = ', mul)
div=num1/num2 #나눗셈 연산 - 소수점 포함 결과 출력
print('div=', div)
#나머지 연산자 - %
div2=num1 % num2
print('div2=', div2)
div3=num1 // num2 #나누기 연산
print('div3=', div3)

#제곱 계산
square = num1 ** 3
print('square=', square)